// Operators

/*
= assign
== compare
=== strict compare

Arithmetic

+ addition
- subtraction
* multiplication
** exponentiation
/ division
% remainder

> and >= 
< and <=

+= add sum
-= subtract sum
*= multiply sum
**= exponentiate sum
/= divide sum

++ increment
-- decrement

Conditonal

&& both must be true
|| one or both be true
! return opposite true or false
*/


console.log(3 + 5); // 8
console.log(5 - 2); // 3
console.log(3 * 5); // 15
console.log(3 ** 3); // 27
console.log(8 / 2); // 4
console.log(10 % 7); // 3

console.log(5 > 2); // true
console.log(5 <= 5); // true


console.log(5 == 5 && 1 == 1); // true
console.log(5 == 5 && 1 == 2); // false

console.log(5 == 4 || 1 == 2); // false
console.log(5 == 5 || 1 == 2); // true